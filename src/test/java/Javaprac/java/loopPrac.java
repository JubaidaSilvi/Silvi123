package Javaprac.java;

public class loopPrac {

	public static void main(String[] args) {
		
		// Print 1 to 5 
		
		 for (int i =1; i<=5; i++)
		 {System.out.println(i);
		 }
		 
		 // add odd number between 1 to 10
		 
		 for(int y=1; y<=10; y=y+2)
		 
		 {System.out.println(y);}
		 
		 
		 //Add even number between 10 to 20
		 
		 for(int s=10; s<=20;s=s+2)
		 {System.out.println(s);}

	}

}
